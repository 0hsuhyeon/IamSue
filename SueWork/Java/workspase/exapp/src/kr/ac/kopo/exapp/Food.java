package kr.ac.kopo.exapp;

public class Food { //푸드라는 타입을 선언
	private String name; // 접근제한자
	private int price;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	

}
